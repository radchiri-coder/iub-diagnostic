// ── Helper ───────────────────────────────────────────────────────────

export function findInterpretation(
  categoryId: CategoryId,
  level: ResultLevel,
  stage: CompanyStage,
): InterpretationBlock | undefined {
  return INTERPRETATIONS.find(
    (b) => b.categoryId === categoryId && b.level === level && b.stage === stage,
  );
}

